/*
	Переменная для хранения модификаторов преобразования скан-кодов в ascii-коды
*/
static u32int mask = 0x00000000;

static int keyboard_scancodes_is_shifted()
{
	if (CheckFlag(mask, CAPSLOCK_PRESSED))
	{
		if (!CheckFlag(mask, LEFT_SHIFT_PRESSED) && !CheckFlag(mask, RIGHT_SHIFT_PRESSED))
			return TRUE;
	}
	else
	{
		if (CheckFlag(mask, LEFT_SHIFT_PRESSED) || CheckFlag(mask, RIGHT_SHIFT_PRESSED))
			return TRUE;
	}
	return FALSE;
}

/*
	Диспетчер клавиатуры
*/
void keyboard_dispatcher()
{
	u8int state;
	u8int code;

	code = inb(0x60);

	switch (code)
	{
	// Обработка клавиши Left Shift
	case KEY_LEFT_SHIFT:
	case KEY_LEFT_SHIFT + 0x80:
		code < 0x80 ? SetFlag(mask, LEFT_SHIFT_PRESSED) : ClearFlag(mask, LEFT_SHIFT_PRESSED);
		break;

	// Обработка клавиши Right Shift
	case KEY_RIGHT_SHIFT:
	case KEY_RIGHT_SHIFT + 0x80:
		code < 0x80 ? SetFlag(mask, RIGHT_SHIFT_PRESSED) : ClearFlag(mask, RIGHT_SHIFT_PRESSED);
		break;

	// Обработка клавиши Caps Lock
	case KEY_CAPSLOCK:
		if (code < 0x80 && !CheckFlag(mask, CAPSLOCK_PRESSED))
			SetFlag(mask, CAPSLOCK_PRESSED);
		else
			ClearFlag(mask, CAPSLOCK_PRESSED);
		break;

	default:
		if (code < 0x80)
		{
			if (keyboard_scancodes_is_shifted())
				code = scancodes_shifted[code];
			else
				code = scancodes[code];

			if (code)
				tty_put_char(code);
		}
		break;
	}
	
	// Читаем байт состояния клавиатуры
	state = inb(0x61);
	state |= 1;

	// Записываем байт состояния клавиатуры
	outb(0x61, state);
}